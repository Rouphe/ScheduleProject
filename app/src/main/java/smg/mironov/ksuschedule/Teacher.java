package smg.mironov.ksuschedule;

public class Teacher {
    private int id;
    private String name;
    private String post;

    //Getter and setters
    public int getId(){
        return id;
    }

    public void setId(int id){
        this.id = id;
    }

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name = name;
    }

    public String getPost(){
        return post;
    }

    public void setPost(String post){
        this.post = post;
    }
}
